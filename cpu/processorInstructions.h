//Byte Codes
//Register arithmetics
#define ADD 0x00
#define SUB 0x01
#define MUL 0x02
#define DIV 0x03
//Register logic
#define AND 0x04
#define NOT 0x05
#define OR 0x06
#define XOR 0x07
//Data access instruction and Register loading
#define LOAD 0x08
#define STOR 0x09
#define PUSH 0x0A
#define POP 0x0B
#define CALL 0x0C
#define DEB 0x0D
#define RET 0x0E
#define JMP 0x0F //unconditional jump
#define PRINT 0x10
// STOP
#define HALT 0x11

